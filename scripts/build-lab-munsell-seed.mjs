/**
 * Generate the palette-v2 re-seed migration for `lab_munsell`.
 *
 * Reads `scripts/data/tiers-512.json` (the 512-chip palette: perceptually-even
 * OKLab farthest-point over the in-gamut Munsell pool, denser in the specialized
 * regions, JND-floored, chroma-floored; tiers 128/256/512 are strict prefixes)
 * and emits ONE migration that:
 *   - adds a unique `color_name` column to lab_munsell + lab_grays,
 *   - AUTHORITATIVELY REPLACES lab_munsell with all 512 chips (palette_index =
 *     array position 0..511) incl. a per-chip `color_name` (color-name-list,
 *     nearest-unused in OKLab — see scripts/lib/color-names.mjs),
 *   - names the 48 grays "System Gray NN" (value-sorted) in SQL,
 *   - drops `iscc_nbs_name` from both tables and the `derive_iscc_nbs_name`
 *     function (the coarse ISCC-NBS bucket naming is retired),
 *   - NULLs project_image_trace.palette_indices_used (old indices are stale),
 *   - refreshes the four column comments that referenced the dropped function.
 *
 * Run: node scripts/build-lab-munsell-seed.mjs <output-sql-path>   (stdout if omitted)
 *
 * This is a real schema change, so the dumped db/schema.sql diff satisfies the
 * schema_sync_check gate on its own (no COMMENT-on-table trick needed).
 */
import { readFileSync, writeFileSync } from "node:fs"
import { resolve, dirname } from "node:path"
import { fileURLToPath } from "node:url"

import { assignChromaticNames } from "./lib/color-names.mjs"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, "..")
const SOURCE_JSON = resolve(REPO_ROOT, "scripts/data/tiers-512.json")

function parseHueFamily(notation) {
  const match = notation.match(/^\s*[\d.]+([A-Z]{1,2})\s/)
  if (!match) throw new Error(`cannot parse hue_family from notation: ${notation}`)
  return match[1]
}

function sqlQuote(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}

function buildSql(entries) {
  const names = assignChromaticNames(entries) // aligned to entries (palette_index order)
  const distinct = new Set(names).size
  if (distinct !== entries.length) {
    throw new Error(`color_name not unique: ${distinct} distinct for ${entries.length} chips`)
  }

  const L = []
  L.push("-- @intent-schema-migration")
  L.push("--")
  L.push("-- Palette v2: reseed lab_munsell + per-chip unique color_name; retire iscc_nbs_name.")
  L.push("-- 512-chip palette = perceptually-even OKLab farthest-point over the in-gamut Munsell")
  L.push("-- pool, denser in the specialized regions (skin/nature/sky/sea), JND-floored so every")
  L.push("-- chip is distinguishable, chroma-floored so none overlap the 48-step lab_grays ramp.")
  L.push("-- palette_index = selection rank 0..511; tiers 128/256/512 are strict prefixes.")
  L.push("--")
  L.push("-- Generated by scripts/build-lab-munsell-seed.mjs — do not hand-edit.")
  L.push("--")
  L.push("-- PRE-CHECK (read-only on prod, must be 0/0/0 before push — TRUNCATE CASCADE would")
  L.push("-- otherwise silently delete dependent rows; re-verify, the prior date is stale):")
  L.push("--   lab_munsell_variants")
  L.push("--   recipes_colors_acryl_schmincke_primacryl WHERE lab_munsell_id IS NOT NULL")
  L.push("--   recipes_colors_oil_schmincke_norma        WHERE lab_munsell_id IS NOT NULL")
  L.push("")
  L.push("-- 0) Unique display-name column on both palettes (nullable; backfilled below).")
  L.push('ALTER TABLE "public"."lab_munsell" ADD COLUMN IF NOT EXISTS "color_name" text;')
  L.push('ALTER TABLE "public"."lab_grays"   ADD COLUMN IF NOT EXISTS "color_name" text;')
  L.push("")
  L.push("-- 1) Authoritative replace of lab_munsell (TRUNCATE CASCADE + INSERT).")
  L.push('TRUNCATE "public"."lab_munsell" CASCADE;')
  L.push("")
  L.push('INSERT INTO "public"."lab_munsell" (')
  L.push('  "notation", "palette_index", "hue_pct", "hue_family",')
  L.push('  "value", "chroma",')
  L.push('  "oklab_l", "oklab_a", "oklab_b",')
  L.push('  "rgb_r", "rgb_g", "rgb_b", "hex", "color_name"')
  L.push(") VALUES")
  const rows = entries.map((entry, i) => {
    const hueFamily = parseHueFamily(entry.munsell)
    const [oklabL, oklabA, oklabB] = entry.oklab
    const [r, g, b] = entry.rgb
    return (
      `  (${sqlQuote(entry.munsell)}, ${i}, ${entry.hue_pct}, ${sqlQuote(hueFamily)},` +
      ` ${entry.value}, ${entry.chroma},` +
      ` ${oklabL}, ${oklabA}, ${oklabB},` +
      ` ${r}, ${g}, ${b}, ${sqlQuote(entry.hex)}, ${sqlQuote(names[i])})`
    )
  })
  L.push(rows.join(",\n") + ";")
  L.push("")
  L.push('-- 2) Grays: uniform "System Gray NN", value-sorted (notation breaks ties).')
  L.push('UPDATE "public"."lab_grays" lg')
  L.push("   SET \"color_name\" = sub.nm")
  L.push("  FROM (")
  L.push("    SELECT \"id\", 'System Gray ' || LPAD(")
  L.push('             ROW_NUMBER() OVER (ORDER BY "value", "notation")::text, 2, \'0\'')
  L.push('           ) AS nm FROM "public"."lab_grays"')
  L.push("  ) sub")
  L.push(" WHERE lg.\"id\" = sub.\"id\";")
  L.push("")
  L.push("-- 3) Enforce the per-chip-name invariant now that every row is backfilled.")
  L.push('ALTER TABLE "public"."lab_munsell" ALTER COLUMN "color_name" SET NOT NULL;')
  L.push('ALTER TABLE "public"."lab_grays"   ALTER COLUMN "color_name" SET NOT NULL;')
  L.push("")
  L.push("-- 4) Old per-trace palette indices reference the previous mapping → drop them.")
  L.push("--    The UI shows a 'Re-run this trace' state for NULL (mobile-colors-sheet).")
  L.push('UPDATE "public"."project_image_trace" SET "palette_indices_used" = NULL;')
  L.push("")
  L.push("-- 5) Retire the ISCC-NBS bucket naming: drop the column (both tables) + the function.")
  L.push('ALTER TABLE "public"."lab_munsell" DROP COLUMN IF EXISTS "iscc_nbs_name";')
  L.push('ALTER TABLE "public"."lab_grays"   DROP COLUMN IF EXISTS "iscc_nbs_name";')
  L.push('DROP FUNCTION IF EXISTS "public"."derive_iscc_nbs_name"(text, numeric, numeric, numeric);')
  L.push("")
  L.push("-- 6) Refresh comments that named the dropped function.")
  for (const col of ["hue_pct", "hue_family", "value", "chroma"]) {
    L.push(
      `COMMENT ON COLUMN "public"."lab_munsell"."${col}" IS ` +
        sqlQuote("consumed by the build-time palette generator (color-lab); not read by app code") +
        ";",
    )
  }
  L.push(
    'COMMENT ON TABLE "public"."lab_munsell" IS ' +
      sqlQuote(
        "512-chip tier palette: perceptually-even OKLab farthest-point over the in-gamut Munsell " +
          "pool, denser in the specialized regions (skin/nature/sky/sea), JND-floored + chroma-floored " +
          "(no overlap with lab_grays). palette_index = selection rank 0..511; tiers 128/256/512 are " +
          "strict prefixes, active tier gated app-side by PALETTE_TIER. color_name = unique display name.",
      ) +
      ";",
  )
  L.push("")
  return L.join("\n")
}

function main() {
  const outputPath = process.argv[2]
  const entries = JSON.parse(readFileSync(SOURCE_JSON, "utf8"))
  if (!Array.isArray(entries) || entries.length === 0) {
    throw new Error(`source JSON empty or invalid: ${SOURCE_JSON}`)
  }
  const sql = buildSql(entries)
  if (outputPath) {
    writeFileSync(outputPath, sql, "utf8")
    console.error(`wrote ${entries.length}-chip palette-v2 re-seed to ${outputPath}`)
  } else {
    process.stdout.write(sql)
  }
}

main()
