/**
 * Generate the seed migration for `lab_munsell` from the color-lab JSON.
 *
 * Reads `color-lab/output/palette-colors.json` (128 entries) and emits a
 * single `INSERT … ON CONFLICT DO NOTHING` migration that seeds the
 * chromatic Lab palette. Idempotent on re-run.
 *
 * Run with:
 *   node scripts/build-lab-munsell-seed.mjs <output-sql-path>
 *
 * If the output path is omitted, prints to stdout.
 *
 * Notes:
 * - `hue_family` is parsed from the Munsell notation (e.g. "2.5R 6/14"
 *   → "R"; "10YR 5/8" → "YR"; "7.5GY 7/10" → "GY"). This is the only
 *   field not present verbatim in the JSON.
 * - `oklab` array is unpacked into `oklab_l`, `oklab_a`, `oklab_b`.
 * - `rgb` array is unpacked into `rgb_r`, `rgb_g`, `rgb_b`.
 */
import { readFileSync, writeFileSync } from "node:fs"
import { resolve, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, "..")
const SOURCE_JSON = resolve(REPO_ROOT, "color-lab/output/palette-colors.json")

function parseHueFamily(notation) {
  // Notation is "<pct><family> <value>/<chroma>", e.g. "2.5R 6/14".
  // Family is the letter sequence immediately after the numeric prefix
  // and before the space. Letters can be 1-2 chars: R, YR, Y, GY, G,
  // BG, B, PB, P, RP.
  const match = notation.match(/^\s*[\d.]+([A-Z]{1,2})\s/)
  if (!match) throw new Error(`cannot parse hue_family from notation: ${notation}`)
  return match[1]
}

function sqlQuote(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}

function buildSql(entries) {
  const lines = []
  lines.push("-- @intent-data-migration")
  lines.push("--")
  lines.push("-- Seed lab_munsell with the 128 chromatic Munsell chips derived from")
  lines.push("-- the color-lab palette (color-lab/output/palette-colors.json).")
  lines.push("-- Generated by scripts/build-lab-munsell-seed.mjs — do not hand-edit;")
  lines.push("-- re-run the loader if the source palette changes.")
  lines.push("--")
  lines.push("-- Idempotent: ON CONFLICT (notation) DO NOTHING.")
  lines.push("")
  lines.push("INSERT INTO \"public\".\"lab_munsell\" (")
  lines.push("  \"notation\", \"palette_index\", \"hue_pct\", \"hue_family\",")
  lines.push("  \"value\", \"chroma\",")
  lines.push("  \"oklab_l\", \"oklab_a\", \"oklab_b\",")
  lines.push("  \"rgb_r\", \"rgb_g\", \"rgb_b\", \"hex\"")
  lines.push(") VALUES")
  const valueRows = entries.map((entry) => {
    const hueFamily = parseHueFamily(entry.munsell)
    const [oklabL, oklabA, oklabB] = entry.oklab
    const [r, g, b] = entry.rgb
    return (
      `  (${sqlQuote(entry.munsell)}, ${entry.index}, ${entry.hue_pct}, ${sqlQuote(hueFamily)},` +
      ` ${entry.value}, ${entry.chroma},` +
      ` ${oklabL}, ${oklabA}, ${oklabB},` +
      ` ${r}, ${g}, ${b}, ${sqlQuote(entry.hex)})`
    )
  })
  lines.push(valueRows.join(",\n"))
  lines.push("ON CONFLICT (\"notation\") DO NOTHING;")
  lines.push("")
  return lines.join("\n")
}

function main() {
  const outputPath = process.argv[2]
  const raw = readFileSync(SOURCE_JSON, "utf8")
  const entries = JSON.parse(raw)
  if (!Array.isArray(entries) || entries.length === 0) {
    throw new Error(`source JSON empty or invalid: ${SOURCE_JSON}`)
  }
  const sql = buildSql(entries)
  if (outputPath) {
    writeFileSync(outputPath, sql, "utf8")
    console.error(`wrote ${entries.length} INSERTs to ${outputPath}`)
  } else {
    process.stdout.write(sql)
  }
}

main()
