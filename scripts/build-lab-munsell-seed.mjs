/**
 * Generate the authoritative re-seed migration for `lab_munsell` from the
 * frozen tier master list.
 *
 * Reads `scripts/data/tiers-512.json` (the two-stage tier palette: a
 * deterministic 512-chip Munsell set, ordered by frequency×spread so the
 * tiers 128/256/512 are prefixes) and emits a single migration that
 * AUTHORITATIVELY REPLACES lab_munsell with all 512 chips, `palette_index`
 * = selection rank (array position 0..511).
 *
 * Run with:
 *   node scripts/build-lab-munsell-seed.mjs <output-sql-path>
 * If the output path is omitted, prints to stdout.
 *
 * Pattern: TRUNCATE … CASCADE + INSERT (no ON CONFLICT) — same as
 * `seed_lab_grays.sql`. CASCADE clears the (currently empty) FK dependents
 * lab_munsell_variants + recipes_colors_*; the migration must only run when
 * those are empty in prod (pre-checked read-only before push).
 *
 * Notes:
 * - `palette_index` is the ARRAY POSITION (selection rank), not `entry.index`.
 * - `hue_family` is parsed from the Munsell notation ("2.5R 6/14" → "R").
 * - `oklab`/`rgb` arrays are unpacked into the *_l/_a/_b and *_r/_g/_b columns.
 * - `iscc_nbs_name` is NOT set per row; it is backfilled via the SQL function
 *   `public.derive_iscc_nbs_name(...)` (the canonical mechanism since #370).
 */
import { readFileSync, writeFileSync } from "node:fs"
import { resolve, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, "..")
const SOURCE_JSON = resolve(REPO_ROOT, "scripts/data/tiers-512.json")

function parseHueFamily(notation) {
  // Notation is "<pct><family> <value>/<chroma>", e.g. "2.5R 6/14".
  // Family is the letter sequence after the numeric prefix: R, YR, Y, GY,
  // G, BG, B, PB, P, RP.
  const match = notation.match(/^\s*[\d.]+([A-Z]{1,2})\s/)
  if (!match) throw new Error(`cannot parse hue_family from notation: ${notation}`)
  return match[1]
}

function sqlQuote(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}

function buildSql(entries) {
  const lines = []
  lines.push("-- @intent-data-migration")
  lines.push("--")
  lines.push("-- Authoritative re-seed of lab_munsell with the 512-chip tier palette")
  lines.push("-- (scripts/data/tiers-512.json). Two-stage: deterministic even Munsell set,")
  lines.push("-- ordered by image frequency × spread → tiers 128/256/512 are prefixes.")
  lines.push("-- palette_index = selection rank (0..511). The app's active tier")
  lines.push("-- (PALETTE_TIER, default 128) limits which prefix is used at runtime.")
  lines.push("--")
  lines.push("-- Generated by scripts/build-lab-munsell-seed.mjs — do not hand-edit;")
  lines.push("-- re-run the loader if the source palette changes.")
  lines.push("--")
  lines.push("-- PRE-CHECK (read-only on prod, must be 0 before push — TRUNCATE CASCADE")
  lines.push("-- would otherwise silently delete dependent rows):")
  lines.push("--   recipes_colors_oil_schmincke_norma WHERE lab_munsell_id IS NOT NULL")
  lines.push("--   recipes_colors_acryl_schmincke_primacryl WHERE lab_munsell_id IS NOT NULL")
  lines.push("--   lab_munsell_variants")
  lines.push("-- Verified 0/0/0 on prod 2026-06-04.")
  lines.push("")
  lines.push("-- 1) Authoritative replace (TRUNCATE-then-INSERT, like seed_lab_grays).")
  lines.push("TRUNCATE \"public\".\"lab_munsell\" CASCADE;")
  lines.push("")
  lines.push("INSERT INTO \"public\".\"lab_munsell\" (")
  lines.push("  \"notation\", \"palette_index\", \"hue_pct\", \"hue_family\",")
  lines.push("  \"value\", \"chroma\",")
  lines.push("  \"oklab_l\", \"oklab_a\", \"oklab_b\",")
  lines.push("  \"rgb_r\", \"rgb_g\", \"rgb_b\", \"hex\"")
  lines.push(") VALUES")
  const valueRows = entries.map((entry, i) => {
    const hueFamily = parseHueFamily(entry.munsell)
    const [oklabL, oklabA, oklabB] = entry.oklab
    const [r, g, b] = entry.rgb
    return (
      `  (${sqlQuote(entry.munsell)}, ${i}, ${entry.hue_pct}, ${sqlQuote(hueFamily)},` +
      ` ${entry.value}, ${entry.chroma},` +
      ` ${oklabL}, ${oklabA}, ${oklabB},` +
      ` ${r}, ${g}, ${b}, ${sqlQuote(entry.hex)})`
    )
  })
  lines.push(valueRows.join(",\n") + ";")
  lines.push("")
  lines.push("-- 2) Backfill ISCC-NBS names via the canonical SQL function (no trigger).")
  lines.push("UPDATE \"public\".\"lab_munsell\"")
  lines.push("   SET \"iscc_nbs_name\" = \"public\".\"derive_iscc_nbs_name\"(")
  lines.push("     \"hue_family\", \"hue_pct\", \"value\", \"chroma\");")
  lines.push("")
  lines.push("-- 3) Old per-trace palette indices reference the previous mapping → drop them.")
  lines.push("--    The UI shows a 'Re-run this trace' state for NULL (mobile-colors-sheet).")
  lines.push("UPDATE \"public\".\"project_image_trace\" SET \"palette_indices_used\" = NULL;")
  lines.push("")
  lines.push("-- 4) COMMENT documents intent AND produces a db/schema.sql diff so the")
  lines.push("--    schema_sync_check CI gate passes for this data-only migration.")
  lines.push(
    "COMMENT ON TABLE \"public\".\"lab_munsell\" IS " +
      sqlQuote(
        `512-chip tier palette (two-stage: deterministic Munsell set + frequency×spread order). ` +
          `palette_index = selection rank 0..511; tiers 128/256/512 are prefixes. ` +
          `Active tier gated app-side by PALETTE_TIER.`,
      ) +
      ";",
  )
  lines.push("")
  return lines.join("\n")
}

function main() {
  const outputPath = process.argv[2]
  const raw = readFileSync(SOURCE_JSON, "utf8")
  const entries = JSON.parse(raw)
  if (!Array.isArray(entries) || entries.length === 0) {
    throw new Error(`source JSON empty or invalid: ${SOURCE_JSON}`)
  }
  const sql = buildSql(entries)
  if (outputPath) {
    writeFileSync(outputPath, sql, "utf8")
    console.error(`wrote ${entries.length}-chip re-seed to ${outputPath}`)
  } else {
    process.stdout.write(sql)
  }
}

main()
