/**
 * Generate the seed migration for `lab_grays` from the color-lab JSON.
 *
 * Reads `color-lab/output/palette-grey.json` (48 entries) and emits a
 * `TRUNCATE ... CASCADE` + `INSERT` migration that authoritatively replaces the
 * table contents (so stale/partial rows can't collide on palette_index).
 *
 * Run with:
 *   node scripts/build-lab-grays-seed.mjs <output-sql-path>
 *
 * If the output path is omitted, prints to stdout.
 */
import { readFileSync, writeFileSync } from "node:fs"
import { resolve, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, "..")
const SOURCE_JSON = resolve(REPO_ROOT, "color-lab/output/palette-grey.json")

function sqlQuote(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}

function buildSql(entries) {
  const lines = []
  lines.push("-- @intent-data-migration")
  // Recognised by scripts/check-types-with-migrations.mjs: pure DML into an
  // existing table has no generated-types surface, so no types regen is due.
  lines.push("-- @intent-no-type-impact")
  lines.push("--")
  lines.push("-- Seed lab_grays with the 48-step N-axis ramp derived from the color-lab")
  lines.push("-- palette (color-lab/output/palette-grey.json). Each row is a valid")
  lines.push("-- Munsell N-value (Munsell-Value formula / ASTM D1535) — the 11")
  lines.push("-- printed book chips (N0/..N10/) plus 37 interpolated fractional")
  lines.push("-- values for finer perceptual resolution.")
  lines.push("--")
  lines.push("-- Generated by scripts/build-lab-grays-seed.mjs — do not hand-edit;")
  lines.push("-- re-run the loader if the source ramp changes.")
  lines.push("--")
  lines.push("-- Authoritative replace: TRUNCATE (CASCADE) then INSERT — clears stale/partial")
  lines.push("-- rows instead of colliding on the palette_index unique key. CASCADE also")
  lines.push("-- clears lab_grays_variants + recipes_grays_* (FK ON DELETE CASCADE).")
  lines.push("")
  lines.push("TRUNCATE TABLE \"public\".\"lab_grays\" CASCADE;")
  lines.push("")
  lines.push("INSERT INTO \"public\".\"lab_grays\" (")
  lines.push("  \"notation\", \"palette_index\", \"value\",")
  lines.push("  \"oklab_l\", \"oklab_a\", \"oklab_b\",")
  lines.push("  \"rgb_r\", \"rgb_g\", \"rgb_b\", \"hex\"")
  lines.push(") VALUES")
  const valueRows = entries.map((entry) => {
    const [oklabL, oklabA, oklabB] = entry.oklab
    const [r, g, b] = entry.rgb
    return (
      `  (${sqlQuote(entry.munsell)}, ${entry.index}, ${entry.value},` +
      ` ${oklabL}, ${oklabA}, ${oklabB},` +
      ` ${r}, ${g}, ${b}, ${sqlQuote(entry.hex)})`
    )
  })
  lines.push(valueRows.join(",\n") + ";")
  lines.push("")
  return lines.join("\n")
}

function main() {
  const outputPath = process.argv[2]
  const raw = readFileSync(SOURCE_JSON, "utf8")
  const entries = JSON.parse(raw)
  if (!Array.isArray(entries) || entries.length === 0) {
    throw new Error(`source JSON empty or invalid: ${SOURCE_JSON}`)
  }
  const sql = buildSql(entries)
  if (outputPath) {
    writeFileSync(outputPath, sql, "utf8")
    console.error(`wrote ${entries.length} INSERTs to ${outputPath}`)
  } else {
    process.stdout.write(sql)
  }
}

main()
