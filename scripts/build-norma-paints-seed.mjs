/**
 * Generate the seed migration for `color_oil_schmincke_norma` and its
 * variants from the legacy Flutter-app SQLite catalog.
 *
 * Reads `import/db/db.sqlite` (untracked) via the system `sqlite3` CLI
 * and maps:
 *   vendor_colors          → color_oil_schmincke_norma
 *   vendor_color_variants  → color_oil_schmincke_norma_variants
 *
 * The legacy `vendor_colors.weight_in_grams` is per-paint but
 * conceptually describes the 35ml tube — it's hoisted onto the 35ml
 * variant row in the new schema. Larger variants (120ml, 200ml) get
 * weight_g NULL until the data is provided.
 *
 * Other fields not in the legacy DB (pigment_codes, lightfastness,
 * opacity) are left NULL and filled by a later enrichment migration.
 *
 * Run with:
 *   node scripts/build-norma-paints-seed.mjs <output-sql-path>
 *
 * If the output path is omitted, prints to stdout.
 */
import { spawnSync } from "node:child_process"
import { existsSync, writeFileSync } from "node:fs"
import { resolve, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, "..")
const SOURCE_DB = resolve(REPO_ROOT, "import/db/db.sqlite")

function sqlQuote(s) {
  if (s === null || s === undefined || s === "") return "NULL"
  return `'${String(s).replace(/'/g, "''")}'`
}

function num(n) {
  if (n === null || n === undefined || n === "") return "NULL"
  return String(n)
}

function querySqlite(query) {
  const res = spawnSync(
    "sqlite3",
    ["-csv", "-header", SOURCE_DB, query],
    { encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] }
  )
  if (res.error) throw new Error(`sqlite3 spawn failed: ${res.error.message}`)
  if (res.status !== 0) throw new Error(`sqlite3 query failed: ${res.stderr}`)
  return parseCsv(res.stdout)
}

function parseCsv(text) {
  const rows = []
  const lines = text.split(/\r?\n/).filter((line) => line.length > 0)
  if (lines.length === 0) return rows
  const header = splitCsvLine(lines[0])
  for (let i = 1; i < lines.length; i++) {
    const cells = splitCsvLine(lines[i])
    const obj = {}
    header.forEach((key, idx) => {
      obj[key] = cells[idx] ?? ""
    })
    rows.push(obj)
  }
  return rows
}

function splitCsvLine(line) {
  const cells = []
  let cur = ""
  let inQuotes = false
  for (let i = 0; i < line.length; i++) {
    const ch = line[i]
    if (inQuotes) {
      if (ch === '"' && line[i + 1] === '"') {
        cur += '"'
        i++
      } else if (ch === '"') {
        inQuotes = false
      } else {
        cur += ch
      }
    } else if (ch === '"') {
      inQuotes = true
    } else if (ch === ",") {
      cells.push(cur)
      cur = ""
    } else {
      cur += ch
    }
  }
  cells.push(cur)
  return cells
}

function buildSql(paints, variants) {
  const lines = []
  lines.push("-- @intent-data-migration")
  lines.push("--")
  lines.push("-- Seed color_oil_schmincke_norma (84 paints) + variants (144 rows)")
  lines.push("-- from the legacy Flutter-app SQLite catalog (import/db/db.sqlite).")
  lines.push("--")
  lines.push("-- Generated by scripts/build-norma-paints-seed.mjs — do not hand-edit;")
  lines.push("-- re-run the loader if the source catalog changes.")
  lines.push("--")
  lines.push("-- vendor_colors.weight_in_grams is hoisted onto the 35ml variant row")
  lines.push("-- (the legacy schema stored it paint-level, but physically the value")
  lines.push("-- belongs to a specific tube size).")
  lines.push("--")
  lines.push("-- Idempotent: ON CONFLICT (code) DO NOTHING and ON CONFLICT")
  lines.push("-- (paint_id, size_ml) DO NOTHING.")
  lines.push("")
  lines.push("BEGIN;")
  lines.push("")

  // Paints
  lines.push("-- ---------------------------------------------------------------------")
  lines.push("-- color_oil_schmincke_norma (84 rows)")
  lines.push("-- ---------------------------------------------------------------------")
  lines.push("INSERT INTO \"public\".\"color_oil_schmincke_norma\" (\"code\", \"name\", \"density_g_per_ml\", \"swatch_storage_path\") VALUES")
  const paintRows = paints.map((p) => {
    const swatchPath = `oil/schmincke/norma/${p.code}.jpg`
    return `  (${sqlQuote(p.code)}, ${sqlQuote(p.name)}, ${num(p.color_density)}, ${sqlQuote(swatchPath)})`
  })
  lines.push(paintRows.join(",\n"))
  lines.push("ON CONFLICT (\"code\") DO NOTHING;")
  lines.push("")

  // Variants: keyed by paint code -> resolve to paint_id via subquery
  // We emit one INSERT per variant using a SELECT FROM color_oil_schmincke_norma
  // so the FK resolves automatically.
  lines.push("-- ---------------------------------------------------------------------")
  lines.push("-- color_oil_schmincke_norma_variants (144 rows)")
  lines.push("-- ---------------------------------------------------------------------")
  // Build a lookup: paint_legacy_id → code
  const codeByLegacyId = new Map(paints.map((p) => [p.id, p.code]))
  // Build a lookup: paint code → weight_in_grams (paint-level legacy data,
  // hoisted to 35ml variant).
  const weightByCode = new Map(
    paints
      .filter((p) => p.weight_in_grams && p.weight_in_grams !== "")
      .map((p) => [p.code, p.weight_in_grams])
  )

  lines.push("INSERT INTO \"public\".\"color_oil_schmincke_norma_variants\" (\"paint_id\", \"size_ml\", \"stock_count\", \"weight_g\")")
  lines.push("SELECT p.id, v.size_ml, v.stock_count, v.weight_g")
  lines.push("FROM (VALUES")
  const variantValueRows = variants.map((v) => {
    const code = codeByLegacyId.get(v.vendor_color_id)
    if (!code) throw new Error(`variant ${v.id} references unknown vendor_color_id ${v.vendor_color_id}`)
    // 35ml variant: use legacy paint-level weight if present.
    // Larger variants: use NULL (data not in legacy DB).
    let weightG = num(v.weight_in_grams)
    if (weightG === "NULL" && String(v.size) === "35") {
      const hoisted = weightByCode.get(code)
      if (hoisted) weightG = num(hoisted)
    }
    return `  (${sqlQuote(code)}, ${v.size}::smallint, ${v.stock}::smallint, ${weightG === "NULL" ? "NULL::numeric" : weightG + "::numeric"})`
  })
  lines.push(variantValueRows.join(",\n"))
  lines.push(") AS v(code, size_ml, stock_count, weight_g)")
  lines.push("JOIN \"public\".\"color_oil_schmincke_norma\" p ON p.code = v.code")
  lines.push("ON CONFLICT (\"paint_id\", \"size_ml\") DO NOTHING;")
  lines.push("")
  lines.push("COMMIT;")
  lines.push("")
  return lines.join("\n")
}

function main() {
  if (!existsSync(SOURCE_DB)) {
    throw new Error(`source SQLite not found: ${SOURCE_DB}`)
  }
  const outputPath = process.argv[2]
  const paints = querySqlite(
    "SELECT id, name, code, weight_in_grams, color_density FROM vendor_colors ORDER BY id"
  )
  const variants = querySqlite(
    "SELECT id, vendor_color_id, size, stock, weight_in_grams FROM vendor_color_variants ORDER BY vendor_color_id, size"
  )
  if (paints.length === 0) throw new Error("no paints found in source")
  const sql = buildSql(paints, variants)
  if (outputPath) {
    writeFileSync(outputPath, sql, "utf8")
    console.error(`wrote ${paints.length} paints + ${variants.length} variants to ${outputPath}`)
  } else {
    process.stdout.write(sql)
  }
}

main()
